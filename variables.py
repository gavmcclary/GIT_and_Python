# This example covers variables

''' Variable: able to be changed or adapted.'''

# A variable is defined using "=" sign.
# The variable name is on the left of the "=" and what it holds is on the right.

my_variable_one = "Python"

my_variable_two = "is"

my_variable_three = "cool!"


print(my_variable_one)

print(my_variable_two)

print(my_variable_three)